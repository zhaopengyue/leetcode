    def insert(v: Int): Boolean = {
        if (map.contains(v)) return false
        data += v
        map += (v -> (data.length - 1))
        true
    }

    def remove(v: Int): Boolean = {
        if (! map.contains(v)) return false
        val index = map(v)
        map -= v
        if (index != data.length - 1) {
            // dataéæœ€åä¸€ä½
            data(index) = data.last
            map(data.last) = index
        }
        data.remove(data.length - 1)
        true
    }

    def getRandom(): Int = {
        val randomIndex = random.nextInt(data.length)
        data(randomIndex)
    }
}

